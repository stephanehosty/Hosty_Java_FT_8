package m02_variables;

public class CharType {

    public static void main(String[] args) {


        char ch1 = 'a';

        System.out.println(ch1);

        //ascii number 97= a
        char ch2 = 97;
        System.out.println(ch2);

        //hexidecimal
        char ch3 = 0x61;

        System.out.println(ch3);

        char ch4 = 45000;

        System.out.println(ch4);


        System.out.println("---------------------------------");

        //char x = 64;
        // char x = 0X40;
        char x = '@';

        System.out.println(x);
    }
}
